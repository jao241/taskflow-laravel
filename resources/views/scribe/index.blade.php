        <h1 id="authenticating-requests">Authenticating requests</h1>
<p>To authenticate requests, include an <strong><code>Authorization</code></strong> header with the value <strong><code>"Bearer 3T2n59OdHgCrAZ59sDNt8I0sJAke8fW7Cf5qAqlQ94c9f893"</code></strong>.</p>
<p>All authenticated endpoints are marked with a <code>requires authentication</code> badge in the documentation below.</p>
<p>You need to add another user if you forgoten your token.</p>

